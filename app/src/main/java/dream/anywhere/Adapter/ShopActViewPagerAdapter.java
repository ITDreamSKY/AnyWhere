package dream.anywhere.Adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;
import android.support.v4.view.PagerAdapter;
import android.view.View;

import java.util.List;

import dream.anywhere.Base.BaseFragment;

/**
 * Created by SKYMAC on 16/9/5.
 */
public class ShopActViewPagerAdapter extends FragmentPagerAdapter {

    private List<BaseFragment> fragments;

    public ShopActViewPagerAdapter(FragmentManager fm, List<BaseFragment> fragments) {
        super(fm);
        this.fragments = fragments;
    }

    @Override
    public Fragment getItem(int position) {
        return fragments.get(position);
    }

    @Override
    public int getCount() {
        return fragments.size();
    }
}
